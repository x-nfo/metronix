<script setup>
import InputError from '@/Components/InputError.vue';
import InputLabel from '@/Components/InputLabel.vue';
import Modal from '@/Components/Modal.vue';
import PrimaryButton from '@/Components/PrimaryButton.vue';
import SecondaryButton from '@/Components/SecondaryButton.vue';
import TextInput from '@/Components/TextInput.vue';
import { useForm } from '@inertiajs/vue3';
import { watchEffect, reactive } from 'vue';
import Checkbox from '@/Components/Checkbox.vue';

const props = defineProps({
    show: Boolean,
    title: String,
    role: Object,
    permissions: Object,
});

const data = reactive({
    multipleSelect: false,
});

const emit = defineEmits(['close']);

const form = useForm({
    name: '',
    permissions: [],
});

const update = () => {
    form.put(route('role.update', props.role?.id), {
        preserveScroll: true,
        onSuccess: () => {
            emit('close');
            form.reset();
            data.multipleSelect = false;
        },
        onError: () => null,
        onFinish: () => null,
    });
};

watchEffect(() => {
    if (props.show) {
        form.name = props.role?.name;
        form.permissions = props.role.permissions?.map((d) => d.id);
    }
    if (props.permissions.length == props.role?.permissions.length) {
        data.multipleSelect = true;
    } else {
        data.multipleSelect = false;
    }
});

const selectAll = (event) => {
    if (event.target.checked === false) {
        form.permissions = [];
    } else {
        form.permissions = [];
        props.permissions.forEach((permission) => {
            form.permissions.push(permission.id);
        });
    }
};
const select = () => {
    if (props.permissions.length == form.permissions.length) {
        data.multipleSelect = true;
    } else {
        data.multipleSelect = false;
    }
};
</script>

<template>
    <section class="space-y-6">
        <Modal id="modal_edit" width="mw-850px">
            <template #title>
                {{ lang().label.edit }} {{ props.title }}
            </template>
            <template #body>
                <form @submit.prevent="update">
                    <div class="my-1">
                        <div class="mb-8">
                            <InputLabel
                                for="name"
                                :value="lang().label.role"
                                :isRequired="true"
                            />
                            <TextInput
                                id="name"
                                type="text"
                                class="mt-1 block w-full"
                                v-model="form.name"
                                required
                                :placeholder="lang().placeholder.name"
                                :error="form.errors.name"
                            />
                            <InputError
                                class="mt-2"
                                :message="form.errors.name"
                            />
                        </div>
                        <div class="mb-8 form-group">
                            <InputLabel
                                :value="lang().label.permission"
                                :isRequired="true"
                            />
                            <InputError
                                class="mt-2"
                                :message="form.errors.permissions"
                            />
                            <div
                                class="form-check d-flex align-items-center col-3"
                            >
                                <Checkbox
                                    id="permission-all"
                                    class="me-2"
                                    v-model:checked="data.multipleSelect"
                                    @change="selectAll"
                                />
                                <InputLabel
                                    for="permission-all"
                                    :value="lang().label.check_all"
                                />
                            </div>
                            <div
                                class="form-check form-check-inline col-3"
                                v-for="(permission, index) in props.permissions"
                                :key="index"
                            >
                                <div
                                    class="form-check form-check-custom form-check-solid"
                                >
                                    <input
                                        @change="select"
                                        class="form-check-input me-2"
                                        type="checkbox"
                                        :id="'permission_' + permission.id"
                                        :value="permission.id"
                                        v-model="form.permissions"
                                    />
                                    <InputLabel
                                        :for="'permission_' + permission.id"
                                        :value="permission.name"
                                    />
                                </div>
                            </div>
                        </div>
                    </div>
                </form>
            </template>
            <template #footer>
                <div class="d-flex gap-2">
                    <SecondaryButton
                        :disabled="form.processing"
                        @click="emit('close')"
                    >
                        {{ lang().button.close }}
                    </SecondaryButton>
                    <PrimaryButton
                        class="ml-3"
                        :class="{ 'opacity-25': form.processing }"
                        :disabled="form.processing"
                        @click="update"
                    >
                        {{
                            form.processing
                                ? lang().button.save + '...'
                                : lang().button.save
                        }}
                    </PrimaryButton>
                </div>
            </template>
        </Modal>
    </section>
</template>
